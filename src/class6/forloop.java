public class forloop {
    public static void main(String[] args) {
        for (int i = 1; i < 11; i++) {
            if (i % 2 == 0) {
                System.out.print(i + " ");
            }
        }
        System.out.println();
        for (int j = 0; j < 11; j++) {
            System.out.println("I love you...");
        }
    }
}
